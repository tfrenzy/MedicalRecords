void prepareFrame(String dee) {
if (dee != null) {
jInternalFrame1.setTitle("Edit Patient Record");
jButton3.setText("Enter Edited Values");
try {
dbase(typedb);
s = conn.createStatement();
preparedStatement = conn.prepareStatement("SELECT * FROM PatientEntry where Name = ?");
preparedStatement.setString(1, dee);
rs = preparedStatement.executeQuery();
while (rs.next()) {
jTextField1.setText(rs.getString("Name"));
jTextField2.setText(rs.getString("Address"));
jTextField3.setText(rs.getString("Occupation"));
jTextField4.setText(rs.getString("Age"));
if (rs.getString("Sex").equals("Male")) {jComboBox1.setSelectedItem("Male");}
else jComboBox1.setSelectedItem("Female");
jTextField5.setText(rs.getString("Allergy"));
}
}
catch (Exception e) {
e.printStackTrace();
}
finally {
try {
conn.close();
s.close();
}
catch (Exception e) {}
}
}
else {
jInternalFrame1.setTitle("New Patient Record");
jButton3.setText("Enter Into System");
jTextField1.setText("");
jTextField2.setText("");
jTextField3.setText("");
jTextField4.setText("");
jTextField5.setText("");
}
}

